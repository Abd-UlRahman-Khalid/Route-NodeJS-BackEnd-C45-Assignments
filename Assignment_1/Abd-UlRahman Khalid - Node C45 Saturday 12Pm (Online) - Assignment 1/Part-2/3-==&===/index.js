/**What are the main differences between == and ===? (0.5 Grade) */

/**
 *
 * the main difference between == and ===
 * that == is only compare between the values and don't concentrate about the data type
 *
 * but === is comapring betweens the values and data type
 *
 * Ex:-
 */

console.log("5" == 5); // the result will be True bec. it just see 5 and 5 not concentrating  wither this 5 is string or integer

console.log("5" === 5); // Now the result will be False bec. yes 5 = 5 but the 1st is string and the other one is integer
